package org.openjdk.jextract.impl;

import org.openjdk.jextract.JavaSourceFile;

import java.util.function.Function;

final class SourceFileBuilder {

    private static final boolean SHOW_GENERATING_CLASS = Boolean.getBoolean("jextract.showGeneratingClass");

    private final String packageName;
    private final String className;

    // code buffer
    private final StringBuilder sb = new StringBuilder();
    // current line alignment (number of 4-spaces)
    private int align;

    private SourceFileBuilder(String packageName, String className) {
        this.packageName = packageName;
        this.className = className;
    }

    public String className() {
        return className;
    }

    public static SourceFileBuilder newSourceFile(String packageName, String className) {
        SourceFileBuilder sfb = new SourceFileBuilder(packageName, className);
        sfb.emitPackagePrefix();
        sfb.emitImportSection();
        return sfb;
    }

    void emitPackagePrefix() {
        assert packageName.indexOf('/') == -1 : "package name invalid: " + packageName;
        appendLines(STR."""
            // Generated by jextract\{SHOW_GENERATING_CLASS ? STR." (via \{getClass().getName()})" : ""}

            """);
        if (!packageName.isEmpty()) {
            appendLines(STR."""
                package \{packageName};

                """);
        }
    }

    void emitImportSection() {
        appendLines("""
            import java.lang.invoke.*;
            import java.lang.foreign.*;
            import java.nio.ByteOrder;
            import java.util.*;
            import java.util.function.*;
            import java.util.stream.*;

            import static java.lang.foreign.ValueLayout.*;
            import static java.lang.foreign.MemoryLayout.PathElement.*;
            """);
    }


    // Internal generation helpers (used by other builders)

    void incrAlign() {
        align++;
    }

    void decrAlign() {
        align--;
    }

    public JavaSourceFile toFile(String suffix, Function<String, String> finisher) {
        return new JavaSourceFile(STR."\{className}\{suffix}", packageName, finisher.apply(sb.toString()));
    }

    public JavaSourceFile toFile(Function<String, String> finisher) {
        return toFile("", finisher);
    }

    public JavaSourceFile toFile() {
        return toFile(s -> s);
    }

    public void appendLines(String s) {
        // we don't just simply use indent here, since that will also indent empty lines which adds trailing whitespace
        String indent = "    ".repeat(align);
        s.lines().map(l -> l.isEmpty() ? "\n" : indent + l + "\n").forEach(sb::append);
    }

    public void appendIndentedLines(String s) {
        incrAlign();
        appendLines(s);
        decrAlign();
    }
}
